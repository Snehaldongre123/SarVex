# Program to add two numbers provided by the user

# Take input from the user and convert to integer
num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))

# Add the two numbers
sum_of_numbers = num1 + num2

# Display the sum using an f-string (formatted string literal)
print(f"The sum of {num1} and {num2} is: {sum_of_numbers}")
